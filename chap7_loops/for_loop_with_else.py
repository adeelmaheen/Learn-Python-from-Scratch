# FOR Loop with Else
# An optional else can be used with a for loop if the code is to be executed when theloops exhasts.
# for loop chalta rahega jb tk usay value milti rahegi or ek br value mil na band hogai tb wo else ko execute krega.

l = [ 1,3,4,5,]
for item in l:
    print(item)
else:
    print("done")       # this is printed when the loop exhausts
